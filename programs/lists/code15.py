# some other functions of list

# index() function
# returns the index of a particular element
list1 = ['som', 'uttam', 'sudipta', 'ghosh', 'uss', 'barrackpore']
print('index() function : ', list1.index('uss'))


# count() function
# Returns the number of elements with the specified value
list2 = ['som', 'uttam', 'sudipta', 'ghosh', 'uss', 'barrackpore', 'som', 'som']
print('count() function : ', list2.count('som'))

# reverse() function
# reverses the whole list
list3 = ['som', 'uttam', 'sudipta', 'ghosh', 'uss', 'barrackpore', 'som', 'som']
list3.reverse()
print('reverse() function : ', list3)